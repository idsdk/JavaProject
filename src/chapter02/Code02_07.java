package chapter02;

public class Code02_07 {
    public static void main(String[] args) {
        int num1, num2, result;

        num1 = 100;
        num2 = 50;

        result = num1 + num2;
        System.out.printf("%d + %d = %d\n", num1, num2, result);

        result = num1 - num2;
        System.out.printf("%d - %d = %d\n", num1, num2, result);

        result = num1 * num2;
        System.out.printf("%d * %d = %d\n", num1, num2, result);

        result = num1 / num2;
        System.out.printf("%d / %d = %d\n", num1, num2, result);

        result = num1 * num2;
        System.out.printf("%d ร %d = %d\n", num1, num2, result);

        result = num1 / num2;
        System.out.printf("%d รท %d = %d\n", num1, num2, result);
    }
}
